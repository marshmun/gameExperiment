package com.objects.archetype;

public class Hunter{

    public String strength;
    public String healthPoints;
    public String magicPoints;
    public String magicStrength;
    public String agilityPoints;
    public String ability;


    public String getStrength() {
        return strength;
    }

    public void setStrength(String strength) {
        this.strength = strength;
    }

    public String getHealthPoints() {
        return healthPoints;
    }

    public void setHealthPoints(String healthPoints) {
        this.healthPoints = healthPoints;
    }

    public String getMagicPoints() {
        return magicPoints;
    }

    public void setMagicPoints(String magicPoints) {
        this.magicPoints = magicPoints;
    }

    public String getMagicStrength() {
        return magicStrength;
    }

    public void setMagicStrength(String magicStrength) {
        this.magicStrength = magicStrength;
    }

    public String getAgilityPoints() {
        return agilityPoints;
    }

    public void setAgilityPoints(String agilityPoints) {
        this.agilityPoints = agilityPoints;
    }

    public String getAbility() {
        return ability;
    }

    public void setAbility(String ability) {
        this.ability = ability;
    }
}