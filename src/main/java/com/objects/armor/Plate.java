
public class Plate{
    public String armorPoints;
    public String agilityPoints;
    public String stengthPoints;
    public String magicPoints;

    public String getArmorPoints() {
        return armorPoints;
    }

    public void setArmorPoints(String armorPoints) {
        this.armorPoints = armorPoints;
    }

    public String getAgilityPoints() {
        return agilityPoints;
    }

    public void setAgilityPoints(String agilityPoints) {
        this.agilityPoints = agilityPoints;
    }

    public String getStengthPoints() {
        return stengthPoints;
    }

    public void setStengthPoints(String stengthPoints) {
        this.stengthPoints = stengthPoints;
    }

    public String getMagicPoints() {
        return magicPoints;
    }

    public void setMagicPoints(String magicPoints) {
        this.magicPoints = magicPoints;
    }
}